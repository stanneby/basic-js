class VigenereCipheringMachine {

  reverse = true;
  constructor(reverse){
    if(reverse == false){
      this.reverse = false;
    }
  }

  encrypt(message, key) {
    if(typeof message != 'string' || typeof key != 'string') throw new Error("Error thrown");
    
    let messageLength = message.length;
    let keyLength = key.length;
    let result = '';
    for(let i = 0, j = 0; i < messageLength; i++, j++){
      let rowIndex = latinAlphabet.indexOf(message.charAt(i).toLowerCase());
      if(rowIndex < 0){
        j--;
        result += message.charAt(i);
        continue; 
      }
      let columnIndex = latinAlphabet.indexOf(key.charAt( j%(keyLength) ).toLowerCase());
      result+= table[rowIndex][columnIndex].toUpperCase();
    }
  
    if( !this.reverse ){
      return result.split('').reverse().join('');
    }
    return result;
  }    
  
  decrypt(encryptedMessage, key) {
    if(typeof encryptedMessage != 'string' || typeof key != 'string') throw new Error("Error thrown");
    
    let result = '';
    let messageLength = encryptedMessage.length;
    let keyLength = key.length;
    for( let i = 0, j = 0; i < messageLength; i++, j++ ){
      let columnIndex = latinAlphabet.indexOf(key.charAt( j%(keyLength) ).toLowerCase());
      let rowIndex = table[columnIndex].indexOf(encryptedMessage.charAt(i).toLowerCase());
      if(rowIndex < 0){
        j--;
        result += encryptedMessage.charAt(i);
        continue; 
      }
      result += latinAlphabet[rowIndex].toUpperCase();
    }
    
    if( !this.reverse ){
      return result.split('').reverse().join('');
    }
    return result;
  }
}

